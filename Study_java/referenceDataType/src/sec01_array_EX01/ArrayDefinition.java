package sec01_array_EX01;

public class ArrayDefinition {
	public static void main(String[] args) {
		
		int[] array1 = new int[3];
		int[] array2;
		array2 = new int[3];

		int array3[] = new int[3];
		int array4[];
		array4 = new int[3];

		// 기본자료형 배열
		boolean[] array5 = new boolean[3];
		double[] array6 = new double[3];
		// 참조자료형 배열
		String[] array7 = new String[9];
	}
}
